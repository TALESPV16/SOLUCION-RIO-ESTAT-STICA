##QUESTÃO RLM5

a) O modelo de regressão linear pode ser extrapolado? 
b) Como definir o domínio de aplicação de um modelo de regressão linear? Justifique sua resposta.

###Resposta letra a)

*Na previsão de novas observações e na estimação da resposta média de um dado ponto, deve-se ser cuidadoso na extrapolção além da região contendo as observações originais, pois é possível que um modelo que se ajuste de forma adequada na região dos dados originais, não se ajuste de forma satisfatória fora daquela faixa.*

